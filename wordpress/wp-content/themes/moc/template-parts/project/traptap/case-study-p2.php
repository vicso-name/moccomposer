                        <p class="masters-comment">TrapTap app is being used on a daily basis to help users avoid
                            speeding tickets and emergency situations, so we needed to make its operation as much
                            trouble-free as it is possible. Tests included many experiments in different locations, with
                            the various internet, Bluetooth and battery conditions. Among others, we found out the most
                            and less battery consuming processes. For example, we were concerned that constantly
                            reconnecting consumes the battery a lot. After running experiments, we found out that it
                            takes only 1% of battery in 10 hours (we assume it isn’t really much for the feature that
                            lets users forget that they have to connect to TrapTap every time they enter a vehicle as it
