            <h2 class="case-section-title text-left">Our Solutions</h2>
            <h5 class="case-study-subtitle">Live Chat integration using Live Chat Inc.</h5>
            <p>Live Chat Inc.’s Facebook Messenger application was not on par with the Facebook API causing us to program a middleware that controlled the inflow and outflow of messages between the chatbot, live chat, and Messenger. It gave us control over the handover protocol, thereby, using different operations like creating a chat, ending chat, etc. at will.</p>
            <h5 class="case-study-subtitle">Personalized retargeting messages</h5>
            <p>The key to personalized retargeting messages was to track user behavior inside the chatbot. So we developed an algorithm that would track every movement of the user inside the bot, the deals they look at and how deep in the flow the users go to. Once we had all the data, we would sort user preferences using different metrics and would retarget the user with a personalized deal that they were most likely to buy.</p>
