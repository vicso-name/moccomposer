    <div class="сhopitle-container">
        <div class="сhopitle-challenge-guts">
            <h2 class="сhopitle-title">Challenge</h2>
            <p>The ability to effectively process the volume of refund requests made by customers to live agents via traditional channels poses an operational challenge to the brand. Foremost among these is the high cost of staffing human agents in a 24/7 contact center.  It is also a user experience problem as, at times, customers must wait for a live agent in order to request a refund while they are already upset, thus causing more frustration with the brand. The implementation of an automated solution is intended to reduce operational costs & customer wait times for refund processing, provide centralization of business logic (to more effectively handle updates to the process), and minimize the risk of human error when processing refunds.</p>
            <p>An additional challenge that impacts the brand for refunds is when users purchase their meal via 3rd-party order and delivery services. In such cases, refunds are not to be provided by the brand but rather by the 3rd-party and that information needed to be effectively relayed to the customer.</p>
        </div>
    </div>
</section>


