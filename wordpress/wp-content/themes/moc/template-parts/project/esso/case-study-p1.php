<section class="esso_results">

    <div class="esso_container">

        <h2 class="esso_section-title">Results</h2>

        <div class="esso_results--content">
            <p>During the five-week campaign, more than <b>84,000</b> Canadians engaged with the Pass the Puck chatbot, with <b>69,896</b> signing up and <b>65,416</b> playing it. This means of all users who signed up, less than 6% left the game during the campaign period. The puck was passed to all three Canadian coasts and all provinces and territories in its first four days of being in production. </p>
            <p>Pucks traveled to more than 2,500 towns, the largest being Toronto, ON and the smallest was in Key West No. 70, SK, a town with a population of just 309 people. The puck traveled more than 1,240,000,000 KMs during the period, or about 31,000+ times around the world. </p>
